                    <strong>1．個人情報の取得について<br></strong>
                    適法かつ公正な手段によって、個人情報を取得致します。<br><br>
                    <strong>2．個人情報の利用について<br></strong>
                    個人情報を、取得の際に示した利用目的の範囲内で、業務の遂行上必要な限りにおいて利用します。<br>
                    また、個人情報を第三者との間で共同利用し、または、個人情報の取扱いを第三者に委託する場合には、当該第三者につき厳正な調査を行ったうえ、秘密を保持させるために、適正な監督を行います。<br><br>
                    <strong>3．個人情報の第三者提供について<br></strong>
                    法令に定める場合を除き、個人情報を事前に本人の同意を得ることなく第三者に提供しません。<br><br>
                    <strong>4．個人情報の管理について<br></strong>
                    個人情報の正確性を保ち、これを安全に管理致します。<br>
                    また、個人情報の紛失、破壊、改ざん及び漏洩などを防止するため、不正アクセス、コンピューターウィルス等に対する適正な情報セキュリティ対策を講じます。<br>
                    当社は、個人情報を持ち出し、外部へ送信する等により漏洩させません。<br><br>
                    <strong>5．個人情報の開示・訂正・利用停止・消去について<br></strong>
                    本人が自己の個人情報について、開示・訂正・利用停止・消去等を求める権利を有していることを確認し、これらの要求がある場合には、異議なく速やかに対応します。<br>
                    なお、当社の個人情報の取扱いにつきましてご意見、ご質問がございましたら、当社個人情報相談窓口までご連絡下さいますようお願い申し上げます。<br><br>
                    <strong>6．組織・体制<br></strong>
                    個人情報保護責任者を任命し、個人情報の適正な管理を実施致します。<br>
                    そして、役員及び従業員に対し、個人情報の保護及び適正な管理方法について研修を実施し、日常業務における個人情報の適正な取扱いを徹底します。<br><br>
                    <strong>7．個人情報保護コンプライアンス･プログラムの策定･実施･維持･改善<br></strong>
